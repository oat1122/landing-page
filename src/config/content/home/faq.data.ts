// Helper function สำหรับสร้าง FAQ Schema
export const generateFAQSchema = () => {
  return FAQ_DATA.map((faq) => ({
    "@type": "Question" as const,
    name: faq.question,
    acceptedAnswer: {
      "@type": "Answer" as const,
      text: faq.answer,
    },
  }));
};
